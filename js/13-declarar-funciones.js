function sumar(numero1 = 0, numero2 = 0) {
    console.log(numero1 + numero2) //lo que esta dentro numero1 y numero2 de la funcion son parametros 
}


sumar(10, 20) //lo que esta por dentro 10 y 20 son argumentos 
sumar(100, 131)
sumar(10)




